class Food {
  final int id;
  final String name;
  final String brand;
  final double protein;
  final double carbs;
  final double fat;
  final double calories;
  final double servingSize;
  final String servingUnits;

  Food({
    required this.id,
    required this.name,
    required this.brand,
    required this.protein,
    required this.carbs,
    required this.fat,
    required this.calories,
    required this.servingSize,
    required this.servingUnits,
  });
}
